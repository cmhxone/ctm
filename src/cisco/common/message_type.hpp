template <>
inline const std::vector<std::byte>
serialize<MessageType>(const MessageType &message_type) {
    return serialize<std::uint32_t>(static_cast<std::uint32_t>(message_type));
}

template <>
inline const MessageType deserialize(const std::vector<std::byte> &bytes) {
    return static_cast<MessageType>(deserialize<std::uint32_t>(bytes));
}

