template <>
inline const std::vector<std::byte> serialize(const TagValue &tag_value) {
    return serialize<std::uint16_t>(static_cast<std::uint16_t>(tag_value));
}

template <>
inline const TagValue deserialize(const std::vector<std::byte> &bytes) {
    return static_cast<TagValue>(deserialize<std::uint16_t>(bytes));
}
